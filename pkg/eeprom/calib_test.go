var calibTestData = []struct {
	raw    []byte
	result [4]wiimote.Vec2
	err    error
}{
	{[]byte{0xa1, 0xaa, 0x8b, 0x99, 0xae, 0x9e, 0x78, 0x30, 0xa7, 0x74, 0xd3}, [4]wiimote.Vec2{{X: 0x0a1, Y: 0x2aa}, {X: 0x399, Y: 0x2ae}, {X: 0x39e, Y: 0x078}, {X: 0x0a7, Y: 0x074}}, nil},
	{[]byte{0xb4, 0xb8, 0xb8, 0xa0, 0xad, 0xb6, 0x66, 0x30, 0xa5, 0x61, 0x18}, [4]wiimote.Vec2{{X: 0x3b4, Y: 0x2b8}, {X: 0x0a0, Y: 0x2ad}, {X: 0x3b6, Y: 0x066}, {X: 0x0a5, Y: 0x061}}, nil},
	{[]byte{0x70, 0x5e, 0x03, 0x7b, 0x50, 0x83, 0x9b, 0xb8, 0x7a, 0xa5, 0xe6}, [4]wiimote.Vec2{{X: 0x070, Y: 0x05e}, {X: 0x37b, Y: 0x050}, {X: 0x383, Y: 0x29b}, {X: 0x07a, Y: 0x2a5}}, nil},
	{[]byte{0x83, 0x9b, 0x80, 0x7e, 0x50, 0x8a, 0x4c, 0x3b, 0x8d, 0x98, 0xf7}, [4]wiimote.Vec2{{X: 0x083, Y: 0x29b}, {X: 0x07e, Y: 0x050}, {X: 0x38a, Y: 0x04c}, {X: 0x38d, Y: 0x298}}, nil},
	{[]byte{0x6f, 0xa9, 0x8b, 0x7d, 0xae, 0x78, 0x5d, 0x03, 0x83, 0x67, 0xe5}, [4]wiimote.Vec2{{X: 0x06f, Y: 0x2a9}, {X: 0x37d, Y: 0x2ae}, {X: 0x078, Y: 0x05d}, {X: 0x383, Y: 0x067}}, nil},

	{[]byte{0xb4, 0xb8, 0xb8, 0xa0, 0xad, 0xb6, 0x66, 0x30, 0xa5, 0x61, 0x08}, [4]wiimote.Vec2{}, ErrCRCMismatch},
}

func TestIRCalib(t *testing.T) {
	for i, tc := range calibTestData {
		t.Run(fmt.Sprintf("case %d", i), func(t *testing.T) {
			r := bytes.NewReader(tc.raw)

			got, err := ReadIRCalibration(r)

			if !errors.Is(err, tc.err) {
				t.Fatalf("expected err %v, got %v", tc.err, err)
			}
			if tc.err != nil {
				return
			}
			if got != tc.result {
				t.Fatalf("mismatch\nexp: %+v\ngot: %+v", tc.result, got)
			}
		})
	}
}
